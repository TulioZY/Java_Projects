public class atividade4 {

    public static void main(String[] args) {
        int a,b,c,d,g,i,f,t,h,p;
        a = 4;
        b = 3;
        c = 10;
        d = 19;
        g = 70;
        i = 13;
        f = (a + b) * (c + d) - (g + i);
        t = (a + b) / 4;
        h = a - 3;
        p = f + t - h;
        System.out.println("O resultado de F e: " + f);
        System.out.println("O resultado de T e: " + t);
        System.out.println("O resultado de H e: " + h);
        System.out.println("O resultado da equacao final (p = f + t - h) e: " + p);
    }
}
